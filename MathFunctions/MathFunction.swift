import Foundation

public class MathFunctions: NSObject {
       
    public func sum(a: Int, b: Int) -> Int {
        return a + b
    }
    
    public func sub(a: Int, b: Int) -> Int {
        return a - b
    }
    
    public func div(a: Int, b: Int) -> Int {
        return a / b
    }
    
    public func mul(a: Int, b: Int) -> Int {
        return a * b
    }
    

    
}
